from concurrent.futures import ThreadPoolExecutor
import time


def worker(name, duration):
    time.sleep(duration)  # Имитация работы (блокировка на duration секунд)
    print(f"{name} завершил работу")
    return f"Результат {name}"  # Возвращаем результат из потока


# АВТОМАТИЧЕСКОЕ управление пулом потоков
with ThreadPoolExecutor(max_workers=3) as executor:
    # Создается пул из 3 потоков, которые будут переиспользоватьс
    # Блок 'with' гарантирует корректное завершение всех потоков

    # Запускаем задачи
    futures = []  # Список для хранения Future объектов (обещаний результатов)
    for i in range(5):
        # Отправляем задачу в пул потоков
        future = executor.submit(worker, f"Работник {i}", i + 1)
        # executor.submit() НЕ блокирует выполнение - задача ставится в очередь
        # future - это "обещание" результата, который будет доступен позже
        futures.append(future)

    # В этот момент:
    # - В пуле 3 активных потока
    # - 5 задач в очереди (2 ждут освобождения потоков)
    # - Все задачи выполняются ПАРАЛЛЕЛЬНО (максимум 3 одновременно)

    # Автоматическое ожидание завершения ВСЕХ задач
    # когда выходим из блока with
    # Блок 'with' НЕ завершится пока все задачи не будут выполнены

# Эта строка выполнится только после завершения ВСЕХ 5 задач
print("Все потоки завершены")  # Выполнится только после ВСЕХ задач

# В этот момент пул потоков автоматически закрывается

# cancelled(): Возвращает True, если задача была отменена до выполнения.
# running(): Возвращает True, если задача в данный момент запущена.
# done(): Возвращает True, если задача завершилась или была отменена.